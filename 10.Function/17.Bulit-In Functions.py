x = "welcome To Python"

print(x.capitalize())

print(x.lower())  #Convert all characters in a string to lowercase

print(x.upper())  #Convert all characters in a string to uppercase

print(len(x))  #Return the length of a string

print(x.replace('Python','Java')) #Replace a specified substring with another string.

print(x.split()) #Split a string into a list of substring based on a delimiter

print(x.count('o')) #Counts the accurrences of a substring in a string.

print(x.title())

